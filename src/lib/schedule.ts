// Helper: convert IST time to UTC ISO string
// IST = UTC+5:30, so subtract 5h30m from IST to get UTC
// Deadline = 5 min before match start
function makeDeadlineUTC(dateStr: string, timeIST: string): string {
  // dateStr: 'YYYY-MM-DD', timeIST: 'HH:MM'
  const [year, month, day] = dateStr.split('-').map(Number);
  const [hour, minute] = timeIST.split(':').map(Number);

  // Convert IST to UTC: subtract 5h30m
  const istDate = new Date(Date.UTC(year, month - 1, day, hour, minute, 0));
  const utcDate = new Date(istDate.getTime() - (5 * 60 + 30) * 60 * 1000);

  // Subtract 5 more minutes for deadline
  const deadlineDate = new Date(utcDate.getTime() - 5 * 60 * 1000);
  return deadlineDate.toISOString();
}

